export async function generateDocx(
  results: TranscriptResult[],
  sourceLabel: string
): Promise<Buffer> {
  const timestamp = new Date().toISOString().slice(0, 19).replace("T", " ");
  const successful = results.filter((r) => r.transcript);
  const failed = results.filter((r) => !r.transcript);

  const children: Paragraph[] = [];

  // Title
  children.push(
    new Paragraph({
      text: "YouTube Transcript Export",
      heading: HeadingLevel.TITLE,
      spacing: { after: 200 },
    })
  );

  // Meta
  children.push(
    new Paragraph({
      children: [
        new TextRun({ text: "Source: ", bold: true, size: 20 }),
        new TextRun({ text: sourceLabel, size: 20 }),
      ],
    })
  );
  children.push(
    new Paragraph({
      children: [
        new TextRun({ text: "Exported: ", bold: true, size: 20 }),
        new TextRun({ text: timestamp, size: 20 }),
      ],
    })
  );
  children.push(
    new Paragraph({
      children: [
        new TextRun({ text: "Videos: ", bold: true, size: 20 }),
        new TextRun({
          text: `${results.length} total, ${successful.length} transcribed, ${failed.length} skipped`,
          size: 20,
        }),
      ],
      spacing: { after: 300 },
    })
  );

  for (const r of results) {
    // Video title
    children.push(
      new Paragraph({
        text: r.title,
        heading: HeadingLevel.HEADING_2,
        spacing: { before: 300, after: 100 },
      })
    );

    // URL
    children.push(
      new Paragraph({
        children: [
          new TextRun({
            text: r.url,
            color: "3264C8",
            size: 18,
          }),
        ],
        spacing: { after: 150 },
      })
    );

    if (r.transcript) {
      // Split into paragraphs to avoid extremely long runs
      const paragraphs = splitIntoParagraphs(r.transcript, 2000);
      for (const p of paragraphs) {
        children.push(
          new Paragraph({
            children: [new TextRun({ text: p, size: 22 })],
            spacing: { after: 80 },
          })
        );
      }
    } else {
      children.push(
        new Paragraph({
          children: [
            new TextRun({
              text: `No transcript available${r.error ? ` (${r.error})` : ""}`,
              italics: true,
              color: "999999",
              size: 22,
            }),
          ],
        })
      );
    }

    // Separator
    children.push(
      new Paragraph({
        children: [
          new TextRun({
            text: "________________________________________",
            color: "DDDDDD",
            size: 16,
          }),
        ],
        alignment: AlignmentType.CENTER,
        spacing: { before: 200, after: 200 },
      })
    );
  }

  // Footer
  children.push(
    new Paragraph({
      children: [
        new TextRun({
          text: "Generated by YT Transcript Extractor -- Built by Christopher Gentile / NewDawn AI",
          italics: true,
          color: "999999",
          size: 16,
        }),
      ],
      spacing: { before: 400 },
      alignment: AlignmentType.CENTER,
    })
  );

  const doc = new Document({
    sections: [{ children }],
  });

  const buffer = await Packer.toBuffer(doc);
  return Buffer.from(buffer);
}

function splitIntoParagraphs(text: string, maxLen: number): string[] {
  if (text.length <= maxLen) return [text];

  const result: string[] = [];
  let remaining = text;

  while (remaining.length > maxLen) {
    let splitAt = remaining.lastIndexOf(". ", maxLen);
    if (splitAt === -1 || splitAt < maxLen / 2) {
      splitAt = remaining.lastIndexOf(" ", maxLen);
    }
    if (splitAt === -1) {
      splitAt = maxLen;
    }
    result.push(remaining.substring(0, splitAt + 1).trim());
    remaining = remaining.substring(splitAt + 1).trim();
  }

  if (remaining) result.push(remaining);
  return result;
}
